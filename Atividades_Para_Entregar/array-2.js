let Listas = ["Pão", "Café", "Leite"]

console.log(Listas)

//push 
    Listas.push("Leite");
    console.log(Listas)

//pop 
    Listas.pop();
    console.log(Listas)

//unshift
    Listas.unshift("Café");
    console.log(Listas);

//shift
    Listas.shift("Leite");
    console.log(Listas);      
    
//indexOf
    console.log(Listas.indexOf("Pão"))

//lastindexOf
    console.log(Listas.lastIndexOf("Pão"))

//join
    console.log(Listas.join("-"));    


